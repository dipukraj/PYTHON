from turtle import *
tracer(2)
bgcolor("black")
pencolor("white")
pensize(3)
def Goto(x, y):
    penup()
    goto(x, y)
    pendown()
Goto(-100, 250)
def iphon(x, y):
    for i in range(2):
        forward(x)
        for i in range(90):
            fd(0.5)
            rt(1)
        forward(y)
        for i in range(90):
            fd(0.5)
            rt(1)
begin_fill()
pencolor("#909090")
fillcolor("#ffffff")
iphon(200, 400)
end_fill()
Goto(-80, 230)
iphon(70, 75)
def camera(f, c):
    begin_fill()
    fillcolor(c)
    for i in range(360):
        fd(f)
        rt(1)
    end_fill()
Goto(-50, 200)
camera(20, "#000000")

